#[cfg(target_arch = "wasm32")]
#[wasm_bindgen::prelude::wasm_bindgen(inline_js = r#"
export function load_lang() {
  try { return globalThis.localStorage?.getItem('protect_carrot_lang') || ''; }
  catch (_) { return ''; }
}
export function save_lang(value) {
  try { globalThis.localStorage?.setItem('protect_carrot_lang', value); }
  catch (_) {}
}
"#)]
extern "C" {
    #[wasm_bindgen::prelude::wasm_bindgen(js_name = load_lang)]
    fn load_lang_js() -> String;
    #[wasm_bindgen::prelude::wasm_bindgen(js_name = save_lang)]
    fn save_lang_js(value: &str);
}

#[cfg(target_arch = "wasm32")]
fn load_lang() -> String {
    load_lang_js()
}

#[cfg(target_arch = "wasm32")]
fn save_lang(value: &str) {
    save_lang_js(value);
}

#[cfg(not(target_arch = "wasm32"))]
fn load_lang() -> String {
    std::fs::read_to_string("tmp/lang.txt").unwrap_or_default()
}

#[cfg(not(target_arch = "wasm32"))]
fn save_lang(value: &str) {
    let _ = std::fs::create_dir_all("tmp");
    let _ = std::fs::write("tmp/lang.txt", value);
}
