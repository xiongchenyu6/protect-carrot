//! Lightweight localization (中文 / English).
//!
//! Every UI string is authored in Chinese at the call site and translated at
//! render time via [`tr`]. The dictionary maps the Chinese source to English;
//! a missing entry falls back to the Chinese text, so untranslated strings stay
//! readable rather than blanking out. The chosen language persists across
//! sessions (localStorage on web, a tmp file natively).

use bevy::prelude::*;
use std::collections::HashMap;
use std::sync::OnceLock;
use std::sync::atomic::{AtomicU8, Ordering};

/// Process-global current language, mirrored from the [`Language`] resource each
/// frame by [`sync_current_lang`]. Lets [`t`] translate at any call site without
/// threading a `lang` parameter through every spawn function. 0 = Zh, 1 = En.
static CURRENT_LANG: AtomicU8 = AtomicU8::new(0);

pub fn set_current_lang(lang: Lang) {
    CURRENT_LANG.store(if lang == Lang::En { 1 } else { 0 }, Ordering::Relaxed);
}

pub fn current_lang() -> Lang {
    if CURRENT_LANG.load(Ordering::Relaxed) == 1 {
        Lang::En
    } else {
        Lang::Zh
    }
}

/// Translate `zh` for the current global language. The ergonomic call-site form:
/// `t("中文")`. Equivalent to `tr(current_lang(), zh)`.
pub fn t(zh: &str) -> String {
    tr(current_lang(), zh)
}

/// Translate a template containing `{}` placeholders, then substitute `args` into
/// each `{}` in order. Use this to localize `format!` strings: the Chinese
/// template (placeholders included) is the dictionary key, so
/// `tf("第{}关 · {}波", &[&n.to_string(), &w.to_string()])` looks up
/// `"第{}关 · {}波"` → `"Level {} · {} waves"`. Pre-format any width/precision
/// specifiers into the arg (`&format!("{:02}", x)`) since the template only
/// supports bare `{}`. Extra `{}` past the args, or extra args, are ignored.
pub fn tf(zh_template: &str, args: &[&str]) -> String {
    let translated = t(zh_template);
    let mut out = String::with_capacity(translated.len());
    let mut rest = translated.as_str();
    let mut it = args.iter();
    while let Some(pos) = rest.find("{}") {
        out.push_str(&rest[..pos]);
        if let Some(a) = it.next() {
            out.push_str(a);
        } else {
            out.push_str("{}");
        }
        rest = &rest[pos + 2..];
    }
    out.push_str(rest);
    out
}

/// Mirror the `Language` resource into the global on change (and once at startup).
pub fn sync_current_lang(lang: Res<Language>) {
    if lang.is_changed() {
        set_current_lang(lang.lang);
    }
}

#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum Lang {
    Zh,
    En,
}

impl Lang {
    /// Display name in the language's own script (for the toggle button).
    pub fn label(self) -> &'static str {
        match self {
            Lang::Zh => "中文",
            Lang::En => "English",
        }
    }

    pub fn next(self) -> Lang {
        match self {
            Lang::Zh => Lang::En,
            Lang::En => Lang::Zh,
        }
    }

    fn tag(self) -> &'static str {
        match self {
            Lang::Zh => "zh",
            Lang::En => "en",
        }
    }

    fn from_tag(tag: &str) -> Option<Lang> {
        match tag.trim() {
            "zh" => Some(Lang::Zh),
            "en" => Some(Lang::En),
            _ => None,
        }
    }
}

#[derive(Resource)]
pub struct Language {
    pub lang: Lang,
}

impl Default for Language {
    fn default() -> Self {
        Self::load()
    }
}

impl Language {
    pub fn load() -> Self {
        let lang = Lang::from_tag(&load_lang()).unwrap_or(Lang::Zh);
        Self { lang }
    }

    pub fn cycle(&mut self) {
        self.lang = self.lang.next();
        save_lang(self.lang.tag());
    }
}

/// Translate a Chinese source string for the given language. Unknown strings
/// (and all strings in Chinese mode) are returned unchanged.
pub fn tr(lang: Lang, zh: &str) -> String {
    match lang {
        Lang::Zh => zh.to_string(),
        Lang::En => dict()
            .get(zh)
            .map(|s| s.to_string())
            .unwrap_or_else(|| zh.to_string()),
    }
}

